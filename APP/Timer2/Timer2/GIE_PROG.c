/****************************************************************/
/* Description    :  This function used to Enable GIE           */
/*					 Inputs : void 								*/
/*					 return : void		 						*/
/****************************************************************/

void GIE_vidEnable (void)
{
	SET_BIT (SREG , GIE_BIT);
	
}

/****************************************************************/
/* Description    :  This function used to Disable GIE          */
/*					 Inputs : void 								*/
/*					 return : void		 						*/
/****************************************************************/

void GIE_vidDisable (void)
{
	CLEAR_BIT(SREG , GIE_BIT);
}






